SELECT * FROM gold.fact_sales

-- Explore all countries our customers come from
SELECT DISTINCT country FROM gold.dim_customers

-- Explore all categories 'The major divisions'
SELECT 
	DISTINCT category, 
	sub_category, 
	product_name
FROM gold.dim_products
ORDER BY 1, 2, 3

-- Find the date of the first and last order
SELECT
	MIN(order_date) AS first_order_date,
	MAX(order_date) AS last_order_date
FROM gold.fact_sales

-- How may years of sales are avaiable
SELECT
	MIN(order_date) AS first_order_date,
	MAX(order_date) AS last_order_date,
    MAX(EXTRACT(YEAR FROM order_date)) 
  - MIN(EXTRACT(YEAR FROM order_date)) AS years_between
FROM gold.fact_sales

-- Find the youngest and the oldest customers
SELECT
	MIN(birth_date) AS oldest_birth_date,
	EXTRACT(YEAR FROM AGE(MIN(birth_date))) AS oldest_age,
	MAX(birth_date) AS youngest_birth_date,
	EXTRACT(YEAR FROM AGE(MAX(birth_date))) AS youngest_age
FROM gold.dim_customers

-- Find the total sales
SELECT SUM(sales) AS total_sales FROM gold.fact_sales

-- Find how many items are sold
SELECT SUM(quantity) AS total_quantity FROM gold.fact_sales

-- Find the average selling price
SELECT AVG(price) AS avg_price FROM gold.fact_sales

-- Find the total numbers of orders
SELECT COUNT(DISTINCT order_number) AS total_orders FROM gold.fact_sales

-- Find the total numbers of products
SELECT COUNT(DISTINCT product_name) AS total_products FROM gold.dim_products

-- Find the total numbers of customers
SELECT COUNT(customer_key) AS total_customers FROM gold.dim_customers

-- Find the total numbers of customers that has placed an order
SELECT COUNT(DISTINCT customer_key) AS total_customers FROM gold.dim_customers

