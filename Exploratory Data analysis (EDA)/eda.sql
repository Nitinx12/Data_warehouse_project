-- Find the total_revenue generated by the category
SELECT
	P.category,
	SUM(S.sales) AS total_revneue
FROM gold.fact_sales AS S
LEFT JOIN gold.dim_products AS P USING(product_key)
GROUP BY 1
ORDER BY 2 DESC

-- What is the total revenue genreated by each customers
SELECT
	C.customer_key,
	CONCAT(C.first_name,' ',C.last_name) AS customer_name,
	SUM(S.sales) AS total_revenue
FROM gold.fact_sales AS S
LEFT JOIN gold.dim_customers AS C 
ON S.customer_key = C.customer_key
GROUP BY 1, 2
ORDER BY 3 DESC

-- What is ths distribution of sold items aross countries
SELECT
	C.country,
	SUM(S.quantity) AS total_sold_items
FROM gold.fact_sales AS S
LEFT JOIN gold.dim_customers AS C
ON C.customer_key = S.customer_key
GROUP BY 1
ORDER BY 2 DESC
	
-- Which 5 products generate the highest revenue
SELECT
	P.product_name,
	SUM(S.sales) AS total_revenue
FROM gold.fact_sales AS S
LEFT JOIN gold.dim_products AS P 
ON P.product_key = S.product_key
GROUP BY 1
ORDER BY 2 DESC
LIMIT 5

-- Which are the 5 worst-performinig products in terms of sales
SELECT
	P.product_name,
	SUM(S.sales) AS total_revenue
FROM gold.fact_sales AS S
LEFT JOIN gold.dim_products AS P 
ON P.product_key = S.product_key
GROUP BY 1
ORDER BY 2 ASC
LIMIT 5

-- find the top 10 customers whow have generated the highest revenue
SELECT *
FROM(SELECT
		C.customer_key,
		CONCAT(C.first_name,' ',C.last_name) AS customer_name,
		COALESCE(SUM(S.sales),0)  AS total_revneue,
		DENSE_RANK()
			OVER(ORDER BY COALESCE(SUM(S.sales),0) DESC) AS rnk
	FROM gold.dim_customers AS C
	LEFT JOIN gold.fact_sales AS S
	ON C.customer_key = S.customer_key
	GROUP BY 1, 2) AS X
WHERE X.rnk <= 10
